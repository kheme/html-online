<p>HTML means HyperText Markup Language, it is the language used to tell a web 
browser how to display information, such as text, images, etc., on a webpage. A 
webpage is a single page on a website and a collection of these webpages linked 
together forms a website. </p>
<p>For a web browser to display a webpage using HTML, your HTML tags have to be 
saved in plain-text using a plain-text editor such as Notepad or ProTx™ and 
saved with the extension &quot;.htm&quot; or &quot;.html&quot; e.g. mypage.htm or mypage.html.</p>
<p>
